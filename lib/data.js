function genSchema(fc) {
  const f = [];
  for (let i = 0; i < Math.min(fc, FREQ.length); i++) f.push({ name: FREQ[i], type: FTYPES[Math.floor(rng() * FTYPES.length)], required: true });
  for (let j = 0; j < fc - FREQ.length && j < FOPT.length; j++) f.push({ name: FOPT[j], type: FTYPES[Math.floor(rng() * FTYPES.length)], required: rng() > 0.6 });
  return f;
}

export function genEndpoints(n) {
  const eps = [];
  let id = 0;
  for (const d of DOMAINS) {
    for (const r of d.resources) {
      if (id >= n) break;
      let method = METHODS[Math.floor(rng() * METHODS.length)];
      if (r.includes("{id}") && method === "POST") method = "PUT";
      if (r === "" && rng() > 0.5) method = "POST";
      const fc = Math.floor(rng() * 40) + 5;
      eps.push({ id: "r" + id, method, path: d.prefix + r, time: TIMES[Math.floor(rng() * TIMES.length)], fieldCount: fc, schema: genSchema(fc) });
      id++;
    }
  }
  while (eps.length < n) {
    const fc = Math.floor(rng() * 30) + 5;
    eps.push({ id: "r" + eps.length, method: METHODS[Math.floor(rng() * METHODS.length)], path: "/api/v2/resource-" + eps.length, time: TIMES[Math.floor(rng() * TIMES.length)], fieldCount: fc, schema: genSchema(fc) });
  }
  return eps.slice(0, n);
}

