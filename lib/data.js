export function genTests(endpoints) {
  const tests = [];
  let id = 0;
  endpoints.forEach((ep) => {
    const count = Math.floor(rng() * 3) + 1;
    for (let i = 0; i < count && id < 400; i++) {
      tests.push({
        id: "t" + id, ep: ep.id,
        name: PROBE_PREFIX[Math.floor(rng() * PROBE_PREFIX.length)] + " " + PROBE_ACTION[Math.floor(rng() * PROBE_ACTION.length)] + " for " + ep.path.split("/").pop().replace("{id}", "record"),
        desc: AI_DESCS[Math.floor(rng() * AI_DESCS.length)],
        pos: Math.floor(rng() * 5) + 1, neg: Math.floor(rng() * 4),
        review: ["approved", "pending", "pending", "approved", "rejected", "pending", "approved", "approved"][Math.floor(rng() * 8)],
        aiConf: Math.floor(rng() * 35) + 65, aiGenerated: true,
      });
      id++;
    }
  });
  return tests;
}

export function genSuites(n, tests) {
  const names = ["smoke", "regression", "e2e", "integration", "nightly", "hourly-health", "load-test", "security-scan", "compliance", "perf-bench", "chaos", "canary", "staging-gate", "prod-verify", "rollback", "dr-test", "migration", "api-contract", "schema-check", "auth-flow"];
  const domainNames = ["patient", "order", "rx", "claim", "inventory", "provider", "auth", "notify", "report", "formulary", "billing", "shipping", "payment", "search", "admin", "audit", "cache", "queue", "webhook", "scheduler"];
  const freqs = ["Hourly", "Daily", "Weekly", "On Deploy", "Bi-Weekly"];
  const suites = [];

  for (let i = 0; i < n; i++) {
    let nm = domainNames[i % domainNames.length] + "-" + names[i % names.length];
    if (i >= 20) nm += "-" + (Math.floor(i / 20) + 1);
    const tIds = [];
    for (let j = 0; j < Math.floor(rng() * 8) + 2; j++) {
      const tid = "t" + Math.floor(rng() * tests.length);
      if (!tIds.includes(tid)) tIds.push(tid);
    }
    const runs = [];
    for (let k = 0; k < Math.floor(rng() * 15) + 3; k++) {
      runs.push({ s: rng() > 0.2 ? "pass" : "fail", d: (Math.floor(rng() * 12) + 1) + "m " + Math.floor(rng() * 59) + "s" });
    }
    const hour = Math.floor(rng() * 24);
    const min = Math.floor(rng() * 4) * 15;
    suites.push({
      id: "s" + i, name: nm, tests: tIds, runs, freq: freqs[Math.floor(rng() * freqs.length)],
      schedule: String(hour).padStart(2, "0") + ":" + String(min).padStart(2, "0") + " UTC",
      lastRun: Math.floor(rng() * 48) + "h ago", nextRun: "Scheduled",
      enabled: rng() > 0.15,
      incident: rng() > 0.92 ? { key: "INC" + Math.floor(rng() * 99999), status: rng() > 0.5 ? "Investigating" : "Monitoring" } : null,
    });
  }
  return suites;
}

